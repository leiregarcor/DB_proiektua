package DB_proiektua.model;

public class PuntuazioAdminModel {
    private String erabIzen;
    private String data;
    private String abeslariIzen;
    private int puntuazioa;

    public String getErabIzen() {
        return erabIzen;
    }

    public void setErabIzen(String erabIzen) {
        this.erabIzen = erabIzen;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getAbeslariIzen() {
        return abeslariIzen;
    }

    public void setAbeslariIzen(String abeslariIzen) {
        this.abeslariIzen = abeslariIzen;
    }

    public int getPuntuazioa() {
        return puntuazioa;
    }

    public void setPuntuazioa(int puntuazioa) {
        this.puntuazioa = puntuazioa;
    }
}
